## dev.php
- git checkout -b dev  创建分支
(Switched to a new branch 'dev')
- git branch     查看分支
- git commit -m '创建分支'
- git push -u origin master  
- git push origin dev    向分支推送
- git checkout master  转回主分支

- git merge dev   合并分支
- git branch -d dev 本地删除分支


- git push origin :dev 远程删除分支